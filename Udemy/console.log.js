// console.log

/* Utilizado para imprimir no terminal ou no console do chrome
Podendo utilizar aspas simples, duplas ou crase
OBS: Sempre que for utilizar variavel com metodo Template Strings ${} no
 meio da string, utiliza-se crase
 string = texto
 */

console.log('Mário Carvalho ')  // Com aspas simples
console.log("Com aspas duplas")
console.log(`Com crase`)

/* para imprimir aspas simples ou duplas na string.
É necessario inverter as aspas na abertura da string
Ex:*/
console.log(" mario 'carvalho' ")
console.log(' mario "carvalho" ')
console.log(` "Mario" 'Carvalho' `)
/*Usando a crase, pode usar qualquer uma das dusas
A utilização no final da linha ou comando passa ser 
opcional para JavaScript
para imprimir numeros, não utiliza-se crase ou aspas
*/
console.log(123456, 1.5)
// podemos imptimir varios tipos tambem separando por virgula
console.log(123, 1.5, 'mario')
    