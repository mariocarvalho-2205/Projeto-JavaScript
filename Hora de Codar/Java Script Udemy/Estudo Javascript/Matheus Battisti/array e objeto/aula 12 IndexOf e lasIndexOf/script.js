// indexOf e lastindexOf

/*
indexOf - encontra o indice de um determinado elemento
lastindexOf - encontra o ultimo indice de um elemento
*/

let numeros = [1, 2, 3, 4, 5, 1, 5, 2, 0]

// retorna o primeiro indice da referencia passada contando do inicio
console.log(numeros.indexOf(4))  


// retorna o ultimo indice da referencia passada
console.log(numeros.lastIndexOf(1))  


